import UIKit

extension UIView {
    
    func universalViewDesign() {
        self.backgroundColor = .white
        self.layer.cornerRadius = 10.0
        self.layer.shadowRadius = 10.0
        self.layer.shadowOpacity = 0.3
    }
    
}
